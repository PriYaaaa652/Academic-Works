"""## Association rules for Comments & Captions"""

def build_rules(itemsets_df, min_conf=0.6, min_lift=1.2):
    rules = association_rules(itemsets_df, metric="confidence", min_threshold=min_conf)
    rules = rules[(rules["confidence"] >= min_conf) & (rules["lift"] >= min_lift)].copy()
    rules["antecedents_str"] = rules["antecedents"].apply(lambda s: ", ".join(sorted(list(s))))
    rules["consequents_str"] = rules["consequents"].apply(lambda s: ", ".join(sorted(list(s))))
    return rules.sort_values(["lift", "confidence"], ascending=False)
