"""## Cluster graph of word associations"""

import os
from collections import Counter
from itertools import combinations
import matplotlib.pyplot as plt
import networkx as nx
from nltk.corpus import stopwords
from networkx.algorithms.community import greedy_modularity_communities


PLOTS_DIR = "./plots"
os.makedirs(PLOTS_DIR, exist_ok=True)
stop_words = set(stopwords.words('english'))
def get_pairs_nostop(transactions):
    pairs_counter = Counter()
    for basket in transactions:
        words = [w for w in basket if w not in stop_words]
        for a, b in combinations(sorted(set(words)), 2):
            pairs_counter[(a, b)] += 1
    return pairs_counter
pairs_comments_nostop = get_pairs_nostop(comments_tx)
pairs_captions_nostop = get_pairs_nostop(captions_tx)
def plot_cluster_graph(pair_counter, top_edges, title, filename):
    top = pair_counter.most_common(top_edges)
    if not top:
        print("No edges to plot:", title)
        return
    G = nx.Graph()
    for (a, b), w in top:
        G.add_edge(a, b, weight=w)

    communities = list(greedy_modularity_communities(G))
    pos = nx.spring_layout(G, seed=42)
    plt.figure(figsize=(10, 10))
    nx.draw_networkx_edges(G, pos, alpha=0.25, edge_color='black')
    for i, comm in enumerate(communities):
        nx.draw_networkx_nodes(
            G, pos,
            nodelist=list(comm),
            node_size=200,
            alpha=0.85,
            label=f"Cluster {i+1}"
        )


    nx.draw_networkx_labels(G, pos, font_size=8, font_color='black')
    plt.title(title)
    plt.axis("off")
    plt.legend(loc="best")
    plt.tight_layout()
    filepath = os.path.join(PLOTS_DIR, filename)
    plt.savefig(filepath, dpi=200)
    plt.show()
    plt.close()
    print("Saved cluster graph:", filepath)
plot_cluster_graph(pairs_comments_nostop, 150, "Cluster graph — Comments", "cluster_comments.png")
plot_cluster_graph(pairs_captions_nostop, 150, "Cluster graph — Captions", "cluster_captions.png")

